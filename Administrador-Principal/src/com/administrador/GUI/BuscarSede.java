    private void llenarItems() {
        //To change body of generated methods, choose Tools | Templates.
        
        listaSedes = buscarSedesClase();
        
        Iterator<Sede> It= listaSedes.iterator();
        
        
        while(It.hasNext()){
            
            this.jComboSedes.addItem(It.next().getNombre());
        
        
        }
    }
    /**
     * buscarSedes
     * @return list
     * busca todas las sedes de una entidad determinada y retorna una lista con ellas
     */
    private static java.util.List<com.esperapp.ws.Sede> buscarSedesClase() {
        com.esperapp.ws.AsignarTurnos_Service service = new com.esperapp.ws.AsignarTurnos_Service();
        com.esperapp.ws.AsignarTurnos port = service.getAsignarTurnosPort();
        return port.buscarSedesClase();
    }

    private static java.util.List<java.lang.String> buscarSedeCodigo(java.lang.String cod) {
        com.esperapp.ws.AsignarTurnos_Service service = new com.esperapp.ws.AsignarTurnos_Service();
        com.esperapp.ws.AsignarTurnos port = service.getAsignarTurnosPort();
        return port.buscarSedeCodigo(cod);
    }

   
