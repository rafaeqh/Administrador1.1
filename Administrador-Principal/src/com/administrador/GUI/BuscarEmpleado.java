    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        this.jLabel6.setVisible(false);
        this.jComboSedes.setVisible(false);
         this.jTextSede.setVisible(true);
                    this.jLabel5.setVisible(true);
        
        List<Empleado> empleados ;
        empleados = null;
        List<String>sedes;
        
        
        
        String cedula= this.jTextCedula.getText();
        Empleado emple;
        
        if(!this.jTextCedula.getText().isEmpty()){
            
            empleados= buscarEmpleado(cedula);
            Iterator<Empleado> It = empleados.iterator();
            if(!empleados.isEmpty()){
                
                if(empleados.size()>1){
                    
                  emple= It.next();
                
   
                    this.jTextCC.setText(emple.getEmpleadoPK().getCedula());
                    this.jTextNombre.setText(emple.getNombre());
                    this.jTextSede.setVisible(false);
                    this.jLabel5.setVisible(false);
                    this.jLabel6.setVisible(true);
                    this.jComboSedes.setVisible(true);
                    
                    sedes = buscarSedeCodigo(emple.getEmpleadoPK().getSede());
                    
                    this.jComboSedes.addItem(sedes.get(2));
                    
                    while(It.hasNext()){
                         emple= It.next();
                         sedes = buscarSedeCodigo(emple.getEmpleadoPK().getSede());
                         this.jComboSedes.addItem(sedes.get(2));
                    }
                    
                }else{
                    emple= It.next();
                
   
                    this.jTextCC.setText(emple.getEmpleadoPK().getCedula());
                    this.jTextNombre.setText(emple.getNombre());
                    sedes = buscarSedeCodigo(emple.getEmpleadoPK().getSede());
                    this.jTextSede.setText(sedes.get(2));
                }
                
            }else{
             
             JOptionPane.showMessageDialog(null ,"Error. La cedula ingresada no existe en la base de datos" ,"Problema proceso",JOptionPane.ERROR_MESSAGE);
            }
            
        }else{
             JOptionPane. showMessageDialog(null ,"Ingrese una cedula a buscar" );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

