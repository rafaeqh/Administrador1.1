    /**
     * buscarEmpleado
     * @param cedula
     * @return list
     * busca un empleado por medio de la cedula recibida por parametro
     */
    private static java.util.List<com.esperapp.ws.Empleado> buscarEmpleado(java.lang.String cedula) {
        com.esperapp.ws.AsignarTurnos_Service service = new com.esperapp.ws.AsignarTurnos_Service();
        com.esperapp.ws.AsignarTurnos port = service.getAsignarTurnosPort();
        return port.buscarEmpleado(cedula);
    }

    /**
     * buscarSedeCodigo
     * @param cod
     * @return list
     * Busca una sede por un codigo recibido por parametro 
     */
    private static java.util.List<java.lang.String> buscarSedeCodigo(java.lang.String cod) {
        com.esperapp.ws.AsignarTurnos_Service service = new com.esperapp.ws.AsignarTurnos_Service();
        com.esperapp.ws.AsignarTurnos port = service.getAsignarTurnosPort();
        return port.buscarSedeCodigo(cod);
    }
