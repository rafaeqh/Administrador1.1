    private void jButtonAgregarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarServicioActionPerformed
        // TODO add your handling code here:
        
        String nombreSede= this.jTextNombreSede.getText();
        String sedeId = this.jTextCodigoSede.getText();
        String tipoServicio = this.jTextServicio.getText();
        boolean[] verificar = new boolean[3];
        verificar[0]= this.jTextCodigoSede.getText().isEmpty();
        verificar[1]= this.jTextServicio.getText().isEmpty();
        List<Servicio> serviciosActuales;
       
       
        
        if(!verificar[0] && !verificar[1]){
            
           if(agregarServicio(sedeId,tipoServicio )){
           
               
               JOptionPane.showMessageDialog(null ,"se agrego el Servicio correctamente" );
              serviciosActuales = buscarServiciosSede(sedeId);
              Iterator<Servicio> It = serviciosActuales.iterator();
               for (int i =this.jComboBoxServiciosExistentes.getItemCount() - 1; i >= 0; i--) {
                   this.jComboBoxServiciosExistentes.removeItemAt(i);
               }
               while(It.hasNext()){
              
                  this.jComboBoxServiciosExistentes.addItem(It.next().getTipo());
              }
     
              
 
           }else{
           
               JOptionPane.showMessageDialog(null ," No se pudo agregar el Servicio " );
           }
            
        
        
        }else{
        
             JOptionPane.showMessageDialog(null ,"Por favor verifique que todos los campos estan llenos" );
        
        }
    }//GEN-LAST:event_jButtonAgregarServicioActionPerformed

