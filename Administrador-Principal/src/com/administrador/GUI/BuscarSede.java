    private void jButtonBuscarSedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarSedeActionPerformed
        // TODO add your handling code here:
        List<String> sede;
        String codigo= this.jTextBCodigo.getText();
       
        
        if (!this.jTextBCodigo.getText().isEmpty()){
            
            sede =  buscarSedeCodigo(codigo);
            
            if(!sede.isEmpty()){
            
            Iterator<String> It= sede.iterator();
            this.jTextEntidad.setText(It.next());
            this.jTextSedeId.setText(It.next());
            this.jTextNombre.setText(It.next());
            this.jTextNombreContacto.setText(It.next());
            this.jTextTelefono.setText(It.next());
            this.jTextCorreo.setText(It.next());
            this.TextDireccion.setText(It.next());
            }else{
                JOptionPane. showMessageDialog(null ,"codigo no valido" );
            }

            
        
        
        
        }else{
            JOptionPane. showMessageDialog(null ,"Ingrese un Nit a buscar" );
        }
        
    }//GEN-LAST:event_jButtonBuscarSedeActionPerformed

