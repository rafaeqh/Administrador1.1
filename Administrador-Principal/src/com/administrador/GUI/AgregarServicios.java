    private static java.util.List<com.esperapp.ws.Servicio> buscarServiciosSede(java.lang.String codigoSede) {
        com.esperapp.ws.AsignarTurnos_Service service = new com.esperapp.ws.AsignarTurnos_Service();
        com.esperapp.ws.AsignarTurnos port = service.getAsignarTurnosPort();
        return port.buscarServiciosSede(codigoSede);
    }

    private static boolean agregarServicio(java.lang.String codigoSede, java.lang.String servicio) {
        com.esperapp.ws.AsignarTurnos_Service service = new com.esperapp.ws.AsignarTurnos_Service();
        com.esperapp.ws.AsignarTurnos port = service.getAsignarTurnosPort();
        return port.agregarServicio(codigoSede, servicio);
    }



   





