    private void jButtonAgregarEntidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarEntidadActionPerformed
        // TODO add your handling code here:
        
        boolean[] campos = new boolean[6];
        String Nit = this.jTextFieldNIT.getText();
        campos[0]=this.jTextFieldNIT.getText().isEmpty();
        String Nombre = this.jTextNombreEntidad.getText();
        campos[1]=this.jTextNombreEntidad.getText().isEmpty();
        String NombreContacto= this.jTextNombreContacto.getText();
        campos[2]=this.jTextNombreContacto.getText().isEmpty();
        String Direccion = this.jTextDireccion.getText();
        campos[3]= this.jTextDireccion.getText().isEmpty();
        String TelefonoContacto = this.jTextTelefonoContacto.getText();
        campos[4]= this.jTextTelefonoContacto.getText().isEmpty();
        String correo = this.jTextCorreoContacto.getText();
        campos[5]= this.jTextCorreoContacto.getText().isEmpty();
       
        
        if(!campos[0] && !campos[1] && !campos[2] && !campos[3] && !campos[4] && !campos[5]){
        
          if( agregarEntidad(Nit,Nombre,NombreContacto,Direccion,TelefonoContacto,correo)){
              
              JOptionPane.showMessageDialog(null ,"La entidad ha sido agregada" );
              
              AgregarSede nuevaSede= new AgregarSede();
              nuevaSede.jTextNIT.setText(Nit);
              nuevaSede.setVisible(true);
              this.setVisible(false);
          
          }else{
               JOptionPane.showMessageDialog(null ,"Error. La entidad con el Nit ingresado ya existe" ,"Problema proceso",JOptionPane.ERROR_MESSAGE);
          }
        
        }else{
            
            JOptionPane.showMessageDialog(null ,"Por favor verifique que todos los campos estan llenos" );
        
        }
    }//GEN-LAST:event_jButtonAgregarEntidadActionPerformed

